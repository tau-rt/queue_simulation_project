def count_lambda_per_hour():
  lambda_per_hour = []
  for hour_weight in config["hour_weights"]:
    lambda_per_hour.append(int(hour_weight/sum(config["hour_weights"])*config["daily_customers"]))
  return lambda_per_hour

def generate_servers(s):
  servers = []
  for i in range(s):
    servers.append(Server(i, 0, 0))
  return servers

def generate_clients_for_day():
  clients = []
  hour_start_time = 0
  next_client_id = 0
  config["lambda_per_hour"] = count_lambda_per_hour()
  clients_per_hour = [int(x) for x in np.random.poisson(config["lambda_per_hour"])]
  for hour in range(len(clients_per_hour)):
    arrival_times = []
    for i in range(clients_per_hour[hour]):
      arrival_times.append(hour_start_time + random.uniform(0,60))
    arrival_times.sort()
    for arrival_time in arrival_times:
      client = Client(next_client_id, arrival_time, None, None)
      clients.append(client)
      next_client_id += 1
    hour_start_time += 60
  return clients, clients_per_hour

def generate_service_time():
  min_t = config["min_service_time"]
  mean_t = config["avg_service_time"]
  max_t = config["max_service_time"]
  scale = mean_t - min_t
  while True:
    service_time = min_t + np.random.exponential(scale)
    if service_time <= max_t:
      return service_time

def check_free_servers(servers):
  for server in servers:
    if server.status == 0:
      return server
  return None

def find_next_free_server(servers):
  busy_servers = []
  for server in servers:
    if server.status == 1:
      busy_servers.append(server)
  if not busy_servers:
    return None, inf
  else:
    next_free_server = min(busy_servers, key=lambda x: x.time_to_free)
    return next_free_server, next_free_server.time_to_free

def check_next_event(next_arrival_time, next_free_server_time):
  if next_arrival_time < next_free_server_time:
    next_event = "arrival"
  elif next_arrival_time == next_free_server_time and next_arrival_time == inf:
    next_event = None
  else:
    next_event = "service_end"
  return next_event

def simulate_day (clients, number_of_servers):
  current_time = 0
  servers = generate_servers(number_of_servers)
  queue = deque([])
  next_arrival_id = 0
  next_arrival_time = clients[next_arrival_id].arrival_time
  while True:
    next_free_server, next_service_end_time = find_next_free_server(servers)
    next_event = check_next_event(next_arrival_time, next_service_end_time)
    if next_event == "arrival":
      current_time = next_arrival_time
      free_server = check_free_servers(servers)
      if free_server is not None:
        free_server.status = 1
        free_server.time_to_free = next_arrival_time + generate_service_time()
        clients[next_arrival_id].service_start_time = current_time
        clients[next_arrival_id].service_end_time = free_server.time_to_free
      else:
        queue.append(clients[next_arrival_id])
      next_arrival_id += 1
      if next_arrival_id == len(clients):
        next_arrival_time = inf
      else:
        next_arrival_time = clients[next_arrival_id].arrival_time
    elif next_event == "service_end":
      current_time = next_free_server.time_to_free
      if queue:
        queue[0].service_start_time = current_time
        queue[0].service_end_time = current_time + generate_service_time()
        next_free_server.status = 1
        next_free_server.time_to_free = queue[0].service_end_time
        queue.popleft()
      else:
        next_free_server.status = 0
        next_free_server.time_to_free = inf
    else:
      break
  return clients

def evaluate_day(clients, clients_per_hour):
  avg_wait_per_hour = []
  max_wait_per_hour = []
  avg_total_time_per_hour = []
  i = 0
  for number_of_clients_per_hour in clients_per_hour:
    avg_wait = 0
    max_wait = 0
    avg_total_time = 0
    if number_of_clients_per_hour == 0:
      avg_wait_per_hour.append(0)
      max_wait_per_hour.append(0)
      avg_total_time_per_hour.append(0)
    else:
      for client_id in range(number_of_clients_per_hour):
        avg_wait += clients[client_id+i].get_queue_time()
        max_wait = max(max_wait, clients[client_id+i].get_queue_time())
        avg_total_time += clients[client_id+i].get_total_time()
      avg_wait_per_hour.append(round((avg_wait/number_of_clients_per_hour), 2))
      max_wait_per_hour.append(round(max_wait,2))
      avg_total_time_per_hour.append(round((avg_total_time/number_of_clients_per_hour),2))
    i += number_of_clients_per_hour
  return avg_wait_per_hour, max_wait_per_hour, avg_total_time_per_hour

def multiple_simulations (number_of_simulations):
  operating_hours = get_operating_hours()
  avg_wait_3d_matrix = np.zeros((number_of_simulations, config["max_number_of_servers"], len(operating_hours)))
  for simulation in range(number_of_simulations):
    clients, clients_per_hour = generate_clients_for_day()
    for number_of_server in range(1, config["max_number_of_servers"]+1):
      for client in clients:
        client.service_start_time = None
        client.service_end_time = None
      clients = simulate_day(clients, number_of_server)
      avg_wait_per_hour, max_wait_per_hour, avg_total_time_per_hour = evaluate_day(clients, clients_per_hour)
      avg_wait_3d_matrix[simulation, number_of_server-1, :] = avg_wait_per_hour
  return avg_wait_3d_matrix

def create_critical_wait_matrix():
  operating_hours = get_operating_hours()
  servers = config["max_number_of_servers"]
  mu = 1/config["avg_service_time"]
  critical_wait_matrix = np.zeros((servers, len(operating_hours)))
  for s in range(1, servers+1):
    for h in range(len(operating_hours)):
      lambda_hour = config["lambda_per_hour"][h]
      rho = lambda_hour/(s*mu*60)
      critical_wait_matrix[s-1, h] = config["critical_avg_wait"]*(1+0.5*rho)
  return critical_wait_matrix

def optimal_number_of_servers(avg_wait_3d_matrix):
  critical_wait_matrix = create_critical_wait_matrix()
  sla_success_matrix = avg_wait_3d_matrix <= critical_wait_matrix
  sla_probability = np.mean(sla_success_matrix, axis=0)
  sla_ok = sla_probability >= config["SLA"]
  required_servers = np.where(np.any(sla_ok, axis=0), np.argmax(sla_ok, axis=0) + 1, -1)
  return required_servers, sla_probability

