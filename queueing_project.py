class Client:
  def __init__(self, id, arrival_time, service_start_time, service_end_time):
    self.id = id
    self.arrival_time = float(arrival_time)
    self.service_start_time = (float(service_start_time) if service_start_time is not None else None)
    self.service_end_time = (float(service_end_time) if service_end_time is not None else None)

  def get_queue_time(self):
    return self.service_start_time - self.arrival_time

  def get_service_time(self):
    return self.service_end_time - self.service_start_time

  def get_total_time(self):
    return self.service_end_time - self.arrival_time

class Server:
  def __init__(self, id, status, time_to_free):
    self.id = id
    self.status = status
    self.time_to_free = time_to_free

